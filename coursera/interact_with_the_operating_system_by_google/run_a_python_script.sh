# Display content of a file
cat my_file.py

# Execute a script
python3 my_file.py

# Add a shebang in a script file
nano my_file.py # Add this at the top of the script before saving the file: #!usr/bin/env python3

# Change the files permissions to make it executable
chmod +x my_file.py

# We can now run the file by prefixing it with a ./
./my_file.py
